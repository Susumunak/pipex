#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!lst || !new)
		return ;
	last = *lst;
	if (!*lst)
		*lst = new;
	else
	{
		last = ft_lstlast(last);
		last->next = new;
	}
}
