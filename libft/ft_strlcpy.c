#include "libft.h"

size_t	ft_strlcpy(char	*dest, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size > 0)
	{
		while (src[i] && --size)
		{
			dest[i] = (char) src[i];
			i++;
		}
		dest[i] = 0;
	}
	return (ft_strlen(src));
}
